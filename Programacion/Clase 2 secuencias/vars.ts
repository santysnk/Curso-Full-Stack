/*
Una variable se crea o declara con la palabra reservada let. A continuación se le declara un nombre.
Luego escribimos el nombre de la variable e indicamos el tipo de dato que va a contener dicha variable,
la cual puede ser de tipo (string, number, boolean... etc).
Por último la inicializamos escribiendo el simbolo de asignacion "=" y luego el valor que va a contener.
*/

let myName: string = "Marcelo Eduardo"  //esto es camel case
console.log("Teacher's name is:" + myName);
